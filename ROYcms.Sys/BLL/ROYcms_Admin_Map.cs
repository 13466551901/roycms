		/// <summary>
		/// 获得数据列表
		/// </summary>
		public List<ROYcms.Sys.Model.ROYcms_Admin_Map> DataTableToList(DataTable dt)
		{
			List<ROYcms.Sys.Model.ROYcms_Admin_Map> modelList = new List<ROYcms.Sys.Model.ROYcms_Admin_Map>();
			int rowsCount = dt.Rows.Count;
			if (rowsCount > 0)
			{
				ROYcms.Sys.Model.ROYcms_Admin_Map model;
				for (int n = 0; n < rowsCount; n++)
				{
					model = new ROYcms.Sys.Model.ROYcms_Admin_Map();
					if(dt.Rows[n]["Id"]!=null && dt.Rows[n]["Id"].ToString()!="")
					{
						model.Id=int.Parse(dt.Rows[n]["Id"].ToString());
					}
					if(dt.Rows[n]["Rid"]!=null && dt.Rows[n]["Rid"].ToString()!="")
					{
						model.Rid=int.Parse(dt.Rows[n]["Rid"].ToString());
					}
					if(dt.Rows[n]["AdminId"]!=null && dt.Rows[n]["AdminId"].ToString()!="")
					{
						model.AdminId=int.Parse(dt.Rows[n]["AdminId"].ToString());
					}
					modelList.Add(model);
				}
			}
			return modelList;
		}

